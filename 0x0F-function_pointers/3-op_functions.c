#include "3-calc.h"

/**
 * op_add - does addition
 * @a: num1
 * @b: num2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - does subtruction
 * @a: num1
 * @b: num2
 * Return: diffrence
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - does multiplication
 * @a: num1
 * @b: num2
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - does division
 * @a: num1
 * @b: num2
 * Return: quetiont
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - does modules
 * @a: num1
 * @b: num2
 * Return: reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
